        if plural == "plans":
            return "Plans"
        elif plural == "planrequests":
            return "Plan requests"
        elif plural == "states":
            return "States"
        elif plural == "providers":
            return "Providers"
        elif plural == "cluster-providers":
            return "Cluster Providers"
        elif plural == "module-template":
            return "Module Template"
        elif plural == "cluster-module-template":
            return "Cluster Module Template"
        elif plural == "modules":
            return "Modules"
